package fr.esiea.tetris2;

public class Piece {

	int type;
	int orientation;
	
	
	public void setOrientation(int orientation){
		this.orientation = orientation;		
	}
	
	
	public int getOrientation(){
		return orientation;		
	}
	
	public void setType(int type){
		this.type = type;		
	}
	
	
	public int getType(){
		return type;		
	}

	
	


}
